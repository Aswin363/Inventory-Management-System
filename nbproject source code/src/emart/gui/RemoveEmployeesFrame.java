    private void loadEmpId() {
         try {
            jcEmpId.removeAllItems(); 
            List<String> empIdList = EmployeesDAO.getAllEmpid();
            if(empIdList.isEmpty()) {
                JOptionPane.showMessageDialog(null, "No employees present!!");
                btnRemove.setEnabled(false);
                return;
            }
            btnRemove.setEnabled(true);
            for(String id : empIdList) {
                jcEmpId.addItem(id);
            }
        } catch(SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    private void clearText() {
        txtEmpName.setText("");
        txtSalary.setText("");
        jcJob.setSelectedIndex(0);
    }
