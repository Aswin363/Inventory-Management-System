    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed
     String empId = (String) jcEmpId.getSelectedItem();
        if (empId == null) {
            JOptionPane.showMessageDialog(null, "Please select an Employee ID!", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            boolean result = EmployeesDAO.deleteEmployees(empId);
            if (result) {
                JOptionPane.showMessageDialog(null, "Employee removed successfully!", "Success", JOptionPane.INFORMATION_MESSAGE);
                loadEmpId(); // Reload IDs
                clearText(); // Fields clear
            } else {
                JOptionPane.showMessageDialog(null, "Employee not found or could not be removed!", "Error", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
          }
        

    }//GEN-LAST:event_btnRemoveActionPerformed

    private void jcEmpIdActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jcEmpIdActionPerformed
        try {
        Object selected = jcEmpId.getSelectedItem();
        if (selected == null) {
            return; // कुछ भी select नहीं हुआ तो बाहर निकल जाएँ
        }

        String empid = selected.toString();
        EmployeesPojo e = EmployeesDAO.findEmpById(empid);

        if (e != null) {
            txtEmpName.setText(e.getEmpname());
            txtSalary.setText(String.valueOf(e.getSalary()));
            jcJob.setSelectedItem(e.getJob());
        }

    } catch (SQLException ex) {
        JOptionPane.showMessageDialog(null, "DB Error: " + ex, "Error", JOptionPane.ERROR_MESSAGE);
        ex.printStackTrace();
    }
    }//GEN-LAST:event_jcEmpIdActionPerformed

  
