    private void btnGenerateBillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGenerateBillActionPerformed
                                                                                                                               
        if (al.isEmpty()) {
            JOptionPane.showMessageDialog(null, 
                "Cart is empty! Add items first.", 
                "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            String ordId = OrderDAO.getNextOrderId();
            boolean orderSaved = OrderDAO.addOrder(al, ordId);

            if (!orderSaved) {
                JOptionPane.showMessageDialog(null, 
                    "Failed to save order to DB.", 
                    "DB Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            boolean stockUpdated = ProductsDAO.updateStocks(al);
            if (!stockUpdated) {
                JOptionPane.showMessageDialog(null, 
                    "Order saved but stock update failed!", 
                    "DB Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String billsDir = "E:\\Bills\\";
            File dir = new File(billsDir);
            if (!dir.exists()) dir.mkdirs();

            String billId = ordId + "_" + System.currentTimeMillis();
            String filePath = billsDir + billId + ".pdf";

            Document doc = new Document();
            FileOutputStream fos = null;

            try {
                fos = new FileOutputStream(filePath);
                PdfWriter.getInstance(doc, fos);
                doc.open();

                // ---------- LOGO SAFE LOADING ----------
                try {
                    String logoPath = "E:\\Bills\\logo.png";
                    File f = new File(logoPath);

                    if (f.exists()) {
                        com.itextpdf.text.Image logo = com.itextpdf.text.Image.getInstance(logoPath);
                        logo.scaleAbsolute(120, 80);
                        logo.setAlignment(Element.ALIGN_CENTER);
                        doc.add(logo);
                    }
                } catch (Exception e) {
                    // ignore but DO NOT STOP PDF
                }

                // ---------- HEADER ----------
                Paragraph header = new Paragraph("\n EMart Supermarket Billing System\n\n");
                header.setAlignment(Element.ALIGN_CENTER);
                doc.add(header);

                Paragraph line = new Paragraph("--------------------------------------------------------------------\n");
                doc.add(line);

                Paragraph details = new Paragraph(
                    "Bill ID : " + billId +
                    "\nDate   : " + new java.util.Date() +
                    "\nCashier: admin" +
                    "\nTotal  : Rs. " + String.format("%.2f", grTotal) + "\n\n"
                );
                doc.add(details);

                // -------- TABLE --------
                PdfPTable table = new PdfPTable(8);
                table.addCell("Product ID");
                table.addCell("Name");
                table.addCell("Price");
                table.addCell("Our Price");
                table.addCell("Company");
                table.addCell("Qty");
                table.addCell("Tax %");
                table.addCell("Sub Total");

                for (ProductsPojo p : al) {
                    table.addCell(p.getProductId());
                    table.addCell(p.getProductName());
                    table.addCell(String.valueOf(p.getProductPrice()));
                    table.addCell(String.valueOf(p.getOurPrice()));
                    table.addCell(p.getProductCompany());
                    table.addCell(String.valueOf(p.getQuantity()));
                    table.addCell(String.valueOf(p.getTax()));
                    table.addCell(String.valueOf(p.getTotal()));
                }

                doc.add(table);
                doc.add(line);

                Paragraph footer = new Paragraph("\nThank You for Shopping with eMart!\n\n");
                footer.setAlignment(Element.ALIGN_CENTER);
                doc.add(footer);

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, 
                    "PDF Error: " + ex.getMessage(), 
                    "Error", JOptionPane.ERROR_MESSAGE);
                ex.printStackTrace();
            } finally {
                try {
                    if (doc.isOpen()) doc.close();
                    if (fos != null) fos.close();
                } catch (Exception e) {}
            }

            // OPEN PDF
            Runtime.getRuntime().exec(new String[]{"cmd", "/c", "start", "", filePath});

            JOptionPane.showMessageDialog(null, 
                "Order of Rs " + String.format("%.2f", grTotal) + 
                "/- created successfully & Bill Generated.");

            this.dispose();
            new ViewOrdersFrame().setVisible(true);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, 
                "DB Error: " + ex.getMessage(), 
                "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }    catch (IOException ex) {
                 Logger.getLogger(BillingFrame.class.getName()).log(Level.SEVERE, null, ex);
             }


    }//GEN-LAST:event_btnGenerateBillActionPerformed

