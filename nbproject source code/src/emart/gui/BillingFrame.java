    private void loadItemList(String pid) {
        try {
            ProductsPojo p = ProductsDAO.getProductDetails(pid);

            if (p.getProductId() == null) {
                JOptionPane.showMessageDialog(null, "Product not found in stock!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int availableQty = p.getQuantity();
            if (availableQty == 0) {
                JOptionPane.showMessageDialog(null, "Product is out of stock!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            String qtyStr = JOptionPane.showInputDialog(null,
                    "Enter quantity for " + p.getProductName() + " (Available: " + availableQty + ")", "Quantity",
                    JOptionPane.PLAIN_MESSAGE);

            if (qtyStr == null || qtyStr.trim().isEmpty()) {
                return;
            }

            int enteredQty;
            try {
                enteredQty = Integer.parseInt(qtyStr.trim());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Invalid quantity entered!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            if (enteredQty <= 0) {
                JOptionPane.showMessageDialog(null, "Quantity must be greater than zero!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            int index = getProductId(pid);
            if (index == -1) {
                if (enteredQty > availableQty) {
                    JOptionPane.showMessageDialog(null, "Only " + availableQty + " units available in stock!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                Object[] row = new Object[8];
                double amount = enteredQty * p.getOurPrice();
                amount += amount * p.getTax() / 100;
                p.setQuantity(enteredQty);
                p.setTotal(amount);

                row[0] = p.getProductId();
                row[1] = p.getProductName();
                row[2] = p.getProductPrice();
                row[3] = p.getOurPrice();
                row[4] = p.getProductCompany();
                row[5] = p.getQuantity();
                row[6] = p.getTax();
                row[7] = p.getTotal();
                tm.addRow(row);

                al.add(p);
                grTotal += p.getTotal();
            } else {
                ProductsPojo existingProduct = al.get(index);
                int newQty = existingProduct.getQuantity() + enteredQty;
                if (newQty > availableQty) {
                    JOptionPane.showMessageDialog(null, "Cannot add more than " + availableQty + " units!", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                }

                double lineAmount = newQty * existingProduct.getOurPrice();
                lineAmount += lineAmount * existingProduct.getTax() / 100;

                existingProduct.setQuantity(newQty);
                existingProduct.setTotal(lineAmount);

                tm.setValueAt(newQty, index, 5);
                tm.setValueAt(lineAmount, index, 7);

                // recalc grand total
                grTotal = al.stream().mapToDouble(ProductsPojo::getTotal).sum();
            }

            lblGrandTotal.setText("Total: " + grTotal);

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, "DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

    private int getProductId(String pid) {
        int index = -1;
        for (int i = 0; i < al.size(); i++) {
            ProductsPojo p = al.get(i);
            if (p.getProductId().equals(pid)) {
                index = i;
                break;
            }
        }
        return index;
    }
