        private void jtProductsMouseClicked(java.awt.event.MouseEvent evt) {
        int row = jtProducts.getSelectedRow();
            if (row == -1) return;
            int choice = JOptionPane.showConfirmDialog(null,
                    "Do you want to remove this item from the cart?",
                    "Remove Item", JOptionPane.YES_NO_OPTION);
            if (choice == JOptionPane.YES_OPTION) {
                double itemTotal = (double) jtProducts.getValueAt(row, 7);
                grTotal -= itemTotal;
                tm.removeRow(row);
                al.remove(row);
                lblGrandTotal.setText("Total: " + grTotal);
                JOptionPane.showMessageDialog(null, "Item removed from cart.");
            }
    }

