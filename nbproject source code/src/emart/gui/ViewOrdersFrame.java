    private void loadOrderIds() 
      {  
          try
          {
               List<String> orderList = OrderDAO.getOrderIdList();
               jcOrderId.removeAllItems();

               if(orderList.isEmpty())
               {
                  JOptionPane.showMessageDialog(null, "No orders present");
                  return;
               }

               for(String id : orderList)
               {
                  jcOrderId.addItem(id);
               }

          }
           catch(SQLException ex)
          {
            JOptionPane.showMessageDialog(null, "DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
    }

