    private void btnBackActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBackActionPerformed
         String userType = UserProfile.getUsertype();
        if(userType == null) {
            JOptionPane.showMessageDialog(null, "User type not found! Please login again.", "Error", JOptionPane.ERROR_MESSAGE);
            new LoginFrame().setVisible(true);
            this.dispose();
            return;
        }

        if(userType.equalsIgnoreCase("Manager")) {
            new ManagerOptionsFrame().setVisible(true);
        } else {
            new ReceptionistOptionFrame().setVisible(true);
        }
        this.dispose();
                                       

    }//GEN-LAST:event_btnBackActionPerformed

    private void btnShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowActionPerformed
       try {
            String orderId = jcOrderId.getSelectedItem().toString();
            List<ProductsPojo> orderDetails = OrderDAO.getOrderDetailsById(orderId);

            DefaultTableModel tm = (DefaultTableModel) jtOrderDetails.getModel();
            tm.setRowCount(0); // Clear previous data

            double grandTotal = 0;

            for(ProductsPojo p : orderDetails)
            {
                Object[] row = new Object[7];
                row[0] = p.getProductId();
                row[1] = p.getProductName();
                row[2] = p.getProductCompany();
                row[3] = p.getProductPrice();
                row[4] = p.getOurPrice();
                row[5] = p.getQuantity();
                row[6] = p.getTax() + "%";
                tm.addRow(row);

                double amt = p.getOurPrice() * p.getQuantity();
                amt += (amt * p.getTax()) / 100;
                grandTotal += amt; 
            }

            lblGrandTotal.setText("Total : " + String.format("%.2f", grandTotal));

        }
        catch(SQLException ex)
          {
            JOptionPane.showMessageDialog(null, "DB Error: " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
          } catch(NullPointerException ex) {
            JOptionPane.showMessageDialog(null, "Please select an order ID!", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnShowActionPerformed

